                        <h5>Coverage Descriptions</h5>
                         <p>
                            <b>Builders Risk</b> - Covers real property while being built, renovated, repaired or tested, as well as personal
                            property that will become a permanent part of the vessel.
                         </p>
                         <p>
                            <b>Cargo</b> - Insures goods, merchandise or commodities of every description which may be carried aboard a vessel, airplane, truck,
                            railcar, or other delivery conveyance.
                         </p>
                         <p>
                            <b>Charterer's Liabilities</b> - Provides insurance for a time charterer, voyage charterer, a charterer in partnership or space charterer
                            in relation to a charter party of an insured vessel.  Charterer's liabilities are the contractual and legal liabilities of the Charterer of a
                            ship.  The main risks are: Liability to cargo, Liability to damages to the ship and Costs made to defend a legal liability claim.
                         </p>
                         <p>
                            <b>Excess/Bumbershoot Liabilities</b> - Provides higher limits of liability insurance that protect against financial loss resulting from a claim
                            for damages because of bodily injury, death, property damage or some other injury for which the insured is allegedly responsible. 
                         </p>
                         <p>
                            <b>Hull & Machinery</b> - Protects shipowners and others with an interest in vessels, machinery attached to the vessel, drilling rigs and the
                            like against the expenses that might be incurred in repairing or replacing such property if it is damaged, destroyed or lost because of a covered peril. 
                         </p>
                         <p>
                            <b>License Defense</b> - Provides legal defense to licensed pilots in the event of U.S. Coast Guard, National Transportation Safety Board and/or
                            State pilotage proceedings as a result of an occurrence.
                         </p>
                         <p>
                            <b>Loss of Income</b> - Provides payment for income continuation in the event of an occurrence which results in confiscation, surrender, suspension,
                            revocation or nonrenewal or prevents the use of an Insured's marine license.
                         </p>
                         <p>
                            <b>Pilot's Legal Liability</b> - Covers liabilities and provides a defense for occurrences which may arise from pilotage of a vessel.
                         </p>
                         <p>
                            <b>Pollution Liability</b> - Covers owners and operators of vessels for exposures to liability for environmental damage and cleanup costs
                            arising from oil spills or the release of other pollutants.
                         </p>
                         <p>
                            <b>Protection & Indemnity</b> - Covers liability exposures of vessel owners or operators against damages arising out of liabilities to third parties,
                            including bodily injury or death of crew members, passengers and others.
                         </p> 
