          <h4 class="style3">History</h4>
          <p class="style1">Marion, one of the oldest towns in Union Parish, was settled by pioneers from Alabama, who named it after their old home county, Marion. Working their way back from Alabama Landing and the Ouachita River, these settlers stopped their westward movement near the site of Marion as early as 1830. The Earliest known family to settle in the vicinity of Marion was David Stewart, who in 1832 built a log cabin four miles south of where Marion now stands. Another family that had settled in this area before Stewart had already left and gone up the Ouachita River to Camden, then dropped back to resettle. Other pioneer families close by where the Whittles, who's negroes whipsawed lumber for families building their new homes in the wilderness; then there were the Repponds and the Briands. The greatest influx of settlers, however, came in the late thirties. The first record of a land sale was on June 13, 1839, to Pascal Traylor from the United States Land Office for $379.00. This Traylor grant today is the center of the town. Other early families who came in before 1850 were the John Traylors, Powells, Larkins. Georges, Greens, Lunsfords, Adamses, Cooks, Hills, and Thomases.</p>
          <p class="style1">The honorable L.E. Thomas, born in Marion in 1866, was one of Union Parish's illustrious sons. He was a descendant  of these pioneers on his father's side Reverend Sampson B. Thomas and his mother's side Reverend Elias George. He became Speaker of the House of Representatives of Louisiana and State Bank Examiner. Later he served as mayor of Shreveport for eight years. </p>
          <p class="style1">Dr. John Traylor is supposed to have built the first store in Marion, and Pascal Traylor the first saw mill. Elias George, one of the foremost Baptist preachers, built the first large house in Marion. The story goes that the preacher purchased for $3000.00 a negro slave, an expert carpenter, to build the house. It was in this house that Anna Portesque Harrison, who had come from New Orleans as a music instructor for the children of the Reverend George, wrote &quot;In the Gloaming&quot;. In 1854 a Mr. Hopkins bought this home. The school system started at Marion shortly before the Civil War. At that time the citizens met and decided to build a school house to be known as the Bell Academy. Work on the building was halted by the outbreak of the war. However, school was carried on in the unfurnished building, with Mr. John Hopkins as instructor. Later the building was torn down, and the Bell Church was used for a second school house. The history of the school from that time until 1905, when Marion built a two-story frame building, was marked by changing from one church to another for holding school. Since 1905 the school has steadily progressed. It was made an accredited high school in 1908. Destroyed by fire in 1922, the two-story frame building was replaced with a modern brick structure. Around 1945, A school for the negro children was begun near the Mount Union Baptist Church. This school was later expanded into what became known as Marion Industrial High School on the site where Oliver Community Center is now located. The school's football team won the 1957 State Championship. After intergration in the 1970's, all children in the Marion area began attending the current high school which won the 2005 State Baseball Championship. Today, with other buildings added, its well-trained teaching force and its large student body, many students coming in by bus from consolidated districts, it ranks with the best in the state.</p>
          <p class="style1">Marion's post office first opened March 18, 1846, was discontinued in 1867, and reopened in 1869. Since then it has operated continuously and furnishes excellent mail service today. There are several thriving churches in Marion: the Methodist, Baptist, and Assembly of God, Marion Baptist, and Steadfast Love Ministries. Marion has a modern hospital, built by the community. It has a sound bank a good drugstore, and several business firms that would be a credit to any small town. Its leading industries are timber, cattle, and poultry. Most of the year in the vicinity of Marion it is estimated that there are one hundred thousand chickens on feed. Commercial fishing in the nearby Ouachita River is done on a fairly large scale, and as a fishing resort for pleasure, this part of the river is almost unsurpassed. With a population of over 800 citizens, it has easy access to the outside world with hard surfaced roads leading out in four directions. </p>
