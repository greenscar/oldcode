			<td height="635"><h4 class="style3">Mayhaw Festival &amp; Pageant </h4>
			  <p class="style2">The Marion Mayhaw Festival began in 1982.&nbsp; The first sponsors were the Marion Jaycees.&nbsp; The 26th annual festival took place on May 11- 12, 2006 and is now sponsored by the Marion Fire Department.&nbsp; The Mayhaw Festival is held on the second weekend of May each year, usually falling on Mother&rsquo;s Day weekend.&nbsp; The 27th annual festival is scheduled for May 9-10, 2008.&nbsp; So, just what is a mayhaw? For our northern neighbors, a definition may be in order.&nbsp; The mayhaw tree is in the Hawthorne family, a distant cousin of the rose.&nbsp; It grows near streams and swamplands bearing fruit that look like small crabapples. The Mayhaw is found in the Southern United States from East Texas to the panhandle of Florida. The heaviest concentrations of native mayhaw trees are found in Grant Parish, Louisiana, near Winnie, Texas and in the Pearl River swamps of Mississippi.&nbsp; The mild difficulty of harvesting wild mayhaw fruit (have your boat handy) is more than compensated by the distinct flavor of the jam, jellies, wines and cakes derived from this unassuming southern de<img src="../images/mayhaw.gif" align="left" width="242" height="185" />light.&nbsp; One taste and you&rsquo;ll understand why we devote an entire festival to this fruit!&nbsp; </p>
			  <p class="style2">Friday night kicks off the festival with a street dance in the parking lot of Marion State Bank.&nbsp; Bring your lawn chairs and dancing shoes to join in the fun!&nbsp; If you&rsquo;re not one for dancing, don&rsquo;t worry- there is plenty of food available as well as some early vendors.&nbsp;Saturday&rsquo;s festivities begin with a parade.&nbsp; Four wheelers, horses, floats, golf carts, and classic cars are all welcome.&nbsp; Bring the kids early to catch all the action- fire trucks, police cars, the Barak Shriners in their mini cars, local businesses tossing candy from floats, and the Mayhaw Pageant queens and contestants throwing prizes.&nbsp; Throughout the day on Saturday, the crowd is entertained by various singers, dancers, cloggers, choirs, and other talent.&nbsp; Activities for all ages are happening all day Saturday.&nbsp; A turkey calling contest, horseshoe tournament, mayhaw spitting contest, egg toss, mayhaw jelly cooking contest, treasure hunt, and a talent show are just a few of the activities you can participate in.&nbsp; You won&rsquo;t want to miss the lawnmower races where high performance lawn mowers are pitted against each other for top prizes and bragging rights.&nbsp; Tons of arts and crafts booths are there for you to browse among.&nbsp; We&rsquo;ve got everything from woodworks to jewelry to candles to hand-painted items and so much more.&nbsp; Find the perfect unique Mother&rsquo;s Day gift.&nbsp; Don&rsquo;t forget to stop by our many food vendors&rsquo; booths as well.&nbsp; Bar-B-Que, fried fish, chicken on a stick, sno cones, crawfish, and funnel cakes to name a few dishes.&nbsp; Eat lunch at the festival and take some home for supper.&nbsp; </p>
			  <p class="style2">Each year, the Mayhaw Pageant is held on the Saturday before the festival.&nbsp; The date for the 2008 Mayhaw Pageant is Saturday, May 3.&nbsp; Girls ages birth to 18 years old are invited to participate, no matter your location.&nbsp; More information will be posted at a later date, so be sure to check back.
		    No matter what you&rsquo;re interested in, you won&rsquo;t be disappointed if you come by the Marion Mayhaw Festival.&nbsp; Come early, bring your friends and make a day of it.&nbsp; You&rsquo;ll be so glad you did!&nbsp; If you have any questions, give Town Hall a call at 318-292-4715</p>
