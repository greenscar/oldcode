    <h4 class="style2">Union Parish Public Library</h4>
    <p class="style3">Union Parish enjoys an excellent parish-supported public library system. The library is located in Farmerville, with a head librarian and a staff of six. Volumes in the library total in excess of 48,753, with approximately 120 new additions per month. The facilities offer reading and reference materials, such as periodicals, films and audio materials for all ages. A bookmobile serves outlaying communities in the parish on a regular basis.  The large collections at the university libraries are available to parish residents. For more info click here for their website: <a href="http://www.youseemore.com/unionparish" target="_blank">http://www.youseemore.com/unionparish</a> * </p>
