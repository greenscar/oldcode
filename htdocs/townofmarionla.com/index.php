          <h4 class="style3">History</h4>
          <p class="style1">Marion, one of the oldest towns in Union Parish, was settled by pioneers from Alabama.  Marion was named after Marion County Alabama the old home community of the first settlers. These new settlers worked their way across what is now known as the Alabama Landing located on the Ouachita River just east of Marion, La.  These settlers paused their westward movement near the present site of Marion as early as 1830. The Earliest known family to settle in the vicinity of Marion was David Stewart family, who in 1832 built a log cabin four miles south of present day Marion. Other families began to settle in this area before Stewart left and traveled up the Ouachita River to Camden. He later returned to resettle in the Marion community. Other pioneer families settling close by where the Whittles, who's negroes whipsawed lumber for families building their new homes in the wilderness.  Additional families arriving were the Repponds and the Briands. The greatest influx of settlers, however, came in the late 1830Õs. The first record of a land sale was on June 13, 1839, to Pascal Traylor issued from the United States Land Office for $379.00. The Traylor grant today is the center of the town. Other early families arriving before 1850 were the John Traylors, Powells, Larkins, Georges, Greens, Lunsfords, Adamses, Cooks, Hills, and Thomases.</p>
          <p class="style1">The honorable L.E. Thomas, born in Marion in 1866, was one of Union Parish's illustrious sons. He was a descendant of these early pioneers on his father's side, the Reverend Sampson B. Thomas and his mother's side, the Reverend Elias George. He became Speaker of the House of Representatives of Louisiana and State Bank Examiner. Later he served as mayor of Shreveport for eight years.</p>
          <p class="style1">Dr. John Traylor is supposed to have built the first store in Marion, and Pascal Traylor the first saw mill. Elias George, one of the foremost Baptist preachers, built the first large house in Marion. The story goes that the preacher purchased a negro slave for $3000.00 who was an master carpenter. His expertise and talents were utilized to build the house. It was in this house that Anna Portesque Harrison, who had arrived from New Orleans as a music instructor for the children of the Reverend George, wrote "In the Gloaming".</p>
          <p class="style1">In 1854 a Mr. Hopkins bought this home. The school system began in Marion shortly before the Civil War. At that time the citizens met and decided to build a school house known as the 'Bell Academy'. Work on the building was halted by the outbreak of the war. However, school was conducted in the unfurnished building, with Mr. John Hopkins as instructor. Later the building was torn down and the Bell Church was used for a second school house. The history of the school from that time until 1905, when Marion built a two-story frame building, was marked by changing from one church to another for holding school. Since 1905 the school has steadily progressed. It was declared an accredited high school in 1908. Destroyed by fire in 1922, the two-story frame building was replaced with a modern brick structure. Around 1945, a school for the negro children in the community was constructed near present day Mount Union Baptist Church. This school was later expanded into what became known as 'Marion Industrial High School' on the site of present day Oliver Community Center. The school's football team won the 1957 State Championship. After integration in 1968, all children in the Marion area began attending present day Marion High School.  Marion High School won the 2005 State Baseball Championship. Today, with other buildings added, its well-trained teaching staff and their large student body, many students coming in by bus from consolidated districts, Marion High School ranks with the best in the state.</p>
          <p class="style1">Marion's post office first opened March 18, 1846.  It was discontinued in 1867 and reopened in 1869. Since 1869 it has operated continuously and furnishes excellent mail service today. There are several thriving churches in Marion: the Methodist, Baptist, and Assembly of God, Marion Baptist, and Steadfast Love Ministries. Marion has a modern hospital, built by and supported by the community. Marion has a sound bank that has recently expanded and an excellent drugstore.  Marion has several other business firms that would be a credit to any small town. Marion leading industries are timber, cattle, and poultry. Most of the year in the vicinity of Marion it is estimated that there are one hundred thousand chickens on feed. Commercial fishing in the nearby Ouachita River is conducted on a fairly large scale and the river is utilized as a fishing resort for pleasure. With a population of over 800 citizens, it has easy access to the outside world with hard surfaced roads leading in four directions.</p>
          <p class="style1">Marion is centrally located between El Dorado, AR to the NW and Crossett, AR to the NE with Ruston, La to the SW and Monroe, LA. to the SE.  Marion is blessed with vast opportunities for post secondary education.  South Arkansas Community College is located in El Dorado, AR. 30 miles to the NW.  Northeast Louisiana Technical College is located in Farmerville, La. just 15 miles west of Marion.  The University of Louisiana Ð Monroe and Community Technical College is 30 miles to the SE and Louisiana Tech University and Grambling State University is located 30 miles to the SW of Marion in Ruston, La.</p>
          <p class="style1">Marion is known as the "Friendly Retirement Community" and is part of the newly recognized "Louisiana Certified Retirement Community" for Union Parish.  For more information, visit the <a href="http://www.tourunionparish.org/index.php?option=com_content&view=article&id=54" style="color:blue">Union Parish Tourist Commision</a> website and view the video for additional information.</p>

