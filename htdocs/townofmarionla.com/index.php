        <p class="style1">The Mayhaw Festival is customarily held on Mother's Day Weekend each year. Family, friends and guests are welcome to attend and taste the famous and delicious Mayhaw jelly.  Mayhaw jelly will taste good on just about anything, especially a hot buttered biscuit. For more information click on the Mayhaw Festival link.</p>
